#!/bin/sh
set -e

echo "ðŸ” CI Lint Preflight - Validating .gitlab-ci.yml"

# Create audit directory
AUDIT_DIR="audit_exports/daily/$(date +%Y%m%d_%H%M%S)"
mkdir -p "${AUDIT_DIR}"

CURRENT_DATE="$(date '+%Y-%m-%d %H:%M:%S UTC')"
ERROR_COUNT=0
WARN_COUNT=0
LINT_OUTPUT=""

# Basic YAML syntax check
echo "ðŸ“‹ Checking YAML syntax..."
if command -v python3 >/dev/null 2>&1; then
    # Try to install PyYAML if not present
    if ! python3 -c "import yaml" 2>/dev/null; then
        echo "ðŸ“¦ Installing PyYAML..."
        pip install PyYAML >/dev/null 2>&1 || echo "âš ï¸  Could not install PyYAML"
    fi
    
    YAML_CHECK=$(python3 -c "
import yaml, sys
try:
    with open('.gitlab-ci.yml', 'r') as f:
        yaml.safe_load(f.read())
    print('âœ… YAML syntax valid')
except yaml.YAMLError as e:
    print(f'âŒ YAML syntax error: {e}')
    sys.exit(1)
except Exception as e:
    print(f'âŒ File read error: {e}')
    sys.exit(1)
" 2>&1)
    YAML_EXIT_CODE=$?
    
    if [ $YAML_EXIT_CODE -ne 0 ]; then
        ERROR_COUNT=$((ERROR_COUNT + 1))
        LINT_OUTPUT="${LINT_OUTPUT}\nâŒ YAML Syntax Error:\n${YAML_CHECK}\n"
        echo "$YAML_CHECK"
    else
        LINT_OUTPUT="${LINT_OUTPUT}\nâœ… YAML Syntax: Valid\n"
        echo "$YAML_CHECK"
    fi
else
    # Fallback: Basic syntax checks without Python
    echo "âš ï¸  Python3 not available - using basic validation"
    
    # Check for basic YAML issues
    if grep -q "^[[:space:]]*:" .gitlab-ci.yml; then
        ERROR_COUNT=$((ERROR_COUNT + 1))
        LINT_OUTPUT="${LINT_OUTPUT}\nâŒ YAML indentation issue detected\n"
        echo "âŒ YAML indentation issue detected"
    elif ! grep -q "^[a-zA-Z]" .gitlab-ci.yml; then
        ERROR_COUNT=$((ERROR_COUNT + 1))
        LINT_OUTPUT="${LINT_OUTPUT}\nâŒ No valid YAML content found\n"
        echo "âŒ No valid YAML content found"
    else
        LINT_OUTPUT="${LINT_OUTPUT}\nâœ… Basic YAML structure: Valid\n"
        echo "âœ… Basic YAML structure appears valid"
    fi
fi

# Basic GitLab CI structure checks
echo "ðŸ“‹ Checking GitLab CI structure..."

# Check for required sections
if ! grep -q "^stages:" .gitlab-ci.yml; then
    ERROR_COUNT=$((ERROR_COUNT + 1))
    LINT_OUTPUT="${LINT_OUTPUT}\nâŒ Missing 'stages:' definition\n"
    echo "âŒ Missing 'stages:' definition"
else
    LINT_OUTPUT="${LINT_OUTPUT}\nâœ… Stages: Defined\n"
    echo "âœ… Stages definition found"
fi

# Check for job naming issues (colons in job names)
COLON_JOBS=$(grep -n "^[a-zA-Z][a-zA-Z0-9_]*:[a-zA-Z]" .gitlab-ci.yml || true)
if [ -n "$COLON_JOBS" ]; then
    ERROR_COUNT=$((ERROR_COUNT + 1))
    LINT_OUTPUT="${LINT_OUTPUT}\nâŒ Jobs with colons in names (schema violation):\n${COLON_JOBS}\n"
    echo "âŒ Found jobs with colons in names:"
    echo "$COLON_JOBS"
else
    LINT_OUTPUT="${LINT_OUTPUT}\nâœ… Job Names: Valid (no schema violations)\n"
    echo "âœ… Job names are schema-compliant"
fi

# Check for script format issues
SCRIPT_ISSUES=$(grep -n "script:" .gitlab-ci.yml | head -5)
if [ -n "$SCRIPT_ISSUES" ]; then
    LINT_OUTPUT="${LINT_OUTPUT}\nðŸ“‹ Script Definitions Found:\n${SCRIPT_ISSUES}\n"
    echo "ðŸ“‹ Found script definitions (manual review recommended)"
fi

# Check for deprecated syntax patterns
DEPRECATED_ONLY=$(grep -n "only:" .gitlab-ci.yml || true)
if [ -n "$DEPRECATED_ONLY" ]; then
    WARN_COUNT=$((WARN_COUNT + 1))
    LINT_OUTPUT="${LINT_OUTPUT}\nâš ï¸  Deprecated 'only:' syntax found (use rules: instead):\n${DEPRECATED_ONLY}\n"
    echo "âš ï¸  Found deprecated 'only:' syntax"
fi

# Generate CI_LINT.md report with required 5 fields
cat > "${AUDIT_DIR}/CI_LINT.md" << EOF
# CI Lint Preflight Report
**Project Path**: ${CI_PROJECT_PATH}
**Project ID**: ${CI_PROJECT_ID}
**Commit SHA**: ${CI_COMMIT_SHORT_SHA}
**Error Count**: ${ERROR_COUNT}
**Warning Count**: ${WARN_COUNT}
**Result**: $([ $ERROR_COUNT -eq 0 ] && echo "PASS" || echo "FAIL")

## Summary
- **Date**: ${CURRENT_DATE}
- **Pipeline**: ${CI_PIPELINE_ID}
- **Status**: $([ $ERROR_COUNT -eq 0 ] && echo "âœ… PASS" || echo "âŒ FAIL")

## Lint Results
${LINT_OUTPUT}

## Recommendations
$([ $ERROR_COUNT -gt 0 ] && echo "ðŸ”§ Fix errors before proceeding with pipeline" || echo "âœ… CI configuration is valid")
$([ $WARN_COUNT -gt 0 ] && echo "ðŸ’¡ Consider addressing warnings for best practices" || echo "")

---
Generated by ZenMarket AI CI Lint Preflight
EOF

echo "ðŸ“„ CI Lint report: ${AUDIT_DIR}/CI_LINT.md"
cat "${AUDIT_DIR}/CI_LINT.md"

# Emit required log lines
echo "LINT_ERRORS=${ERROR_COUNT}"
echo "RESULT=$([ $ERROR_COUNT -eq 0 ] && echo "PASS" || echo "FAIL")"

# Exit with error if any errors found
if [ $ERROR_COUNT -gt 0 ]; then
    echo "âŒ CI Lint failed with ${ERROR_COUNT} errors"
    exit 1
else
    echo "âœ… CI Lint passed with ${WARN_COUNT} warnings"
fi