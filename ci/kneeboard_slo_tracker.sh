# Get current times for comparison
CURRENT_HOUR=$(date '+%H')
CURRENT_MIN=$(date '+%M')
CURRENT_TOTAL_MIN=$((CURRENT_HOUR * 60 + CURRENT_MIN))

AM_TARGET_MIN=$((9 * 60))      # 09:00 = 540 minutes
AM_MACRO_MIN=$((9 * 60 + 15))  # 09:15 = 555 minutes  
