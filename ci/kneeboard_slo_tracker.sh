    if [ $CURRENT_TOTAL_MIN -le $AM_TARGET_MIN ]; then
        SLO_AM="PASS"
        echo "âœ… AM kneeboard: On-time delivery window"
    elif [ $CURRENT_TOTAL_MIN -le $AM_MACRO_MIN ]; then
        SLO_AM="PASS"
